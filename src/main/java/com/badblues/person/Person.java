package com.badblues.person;


abstract class Person {
    
    //values between 0 and 100
    private int x;
    private int y;

    public Person(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public void setX(int x) {
        this.x = x;
    }

    public void setY(int y) {
        this.y = y;
    }
}